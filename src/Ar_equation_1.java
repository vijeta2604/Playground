public class Ar_equation_1 {
    public static void main(String[] args) {
        float a=7/4 * 9/2;
        System.out.println(a);
        float b=7/4 + 9/2;
        System.out.println(b);
        float c=7/4f + 9/2f;
        System.out.println(c);
        float d=8/4f * 8/2f;
        System.out.println(d);
        float e=8/4 * 8/2;
        System.out.println(e);

    }
}
